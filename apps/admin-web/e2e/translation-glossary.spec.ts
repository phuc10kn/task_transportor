import { expect, test } from "@playwright/test";
import { login, mockAuth, project } from "./support/phase-fixtures";

test("Translation Glossary lazy-loads selected project and opens dynamic-language editor", async ({ page }) => {
  await mockAuth(page);
  let concepts = [{ id: 4, project_id: 1, group_key: "default", concept_key: "staging", note: "release label", terms: [{ id: 1, language_code: "ja", term: "ステージング", is_canonical: true }, { id: 3, language_code: "ja", term: "STAGING", is_canonical: false }, { id: 2, language_code: "vi", term: "STG", is_canonical: true }] }];
  let postedConcept: Record<string, unknown> | null = null;
  let deleteCalls = 0;
  await page.route("**/api/v1/projects", (route) => route.fulfill({ status: 200, contentType: "application/json", body: JSON.stringify({ data: [project] }) }));
  await page.route("**/api/v1/projects/1/translation-glossary", (route) => route.fulfill({ status: 200, contentType: "application/json", body: JSON.stringify({ data: { project_id: 1, concepts } }) }));
  await page.route("**/api/v1/projects/1/translation-glossary/concepts", async (route) => {
    if (route.request().method() !== "POST") return route.continue();
    postedConcept = route.request().postDataJSON() as Record<string, unknown>;
    if (postedConcept.concept_key === "conflict") return route.fulfill({ status: 409, contentType: "application/json", body: JSON.stringify({ error: { code: "GLOSSARY_CONFLICT", message: "Concept key already exists." } }) });
    const terms = (postedConcept.terms as Array<Record<string, unknown>>) || [];
    concepts = [...concepts, { id: 5, project_id: 1, group_key: String(postedConcept.group_key), concept_key: String(postedConcept.concept_key), note: String(postedConcept.note || ""), terms: terms.map((term, index) => ({ id: 10 + index, language_code: String(term.language_code), term: String(term.term), is_canonical: Boolean(term.is_canonical) })) }];
    return route.fulfill({ status: 201, contentType: "application/json", body: JSON.stringify({ data: concepts[concepts.length - 1] }) });
  });
  await page.route("**/api/v1/projects/1/translation-glossary/concepts/5", async (route) => {
    if (route.request().method() !== "DELETE") return route.continue();
    deleteCalls += 1; concepts = concepts.filter((concept) => concept.id !== 5);
    return route.fulfill({ status: 204, body: "" });
  });
  await login(page, "/translation-glossary?project_id=1");
  await expect(page.getByRole("heading", { name: "Glossary" })).toBeVisible();
  await expect(page.getByRole("cell", { name: "staging", exact: true })).toBeVisible();
  await page.getByRole("button", { name: "staging", exact: true }).click();
  const viewDialog = page.getByRole("dialog", { name: "View glossary concept" });
  await expect(viewDialog).toContainText("Languages");
  await expect(viewDialog).toContainText("ja: ステージング (Canonical)");
  await expect(viewDialog).toContainText("vi: STG");
  await expect(viewDialog).not.toContainText("vi: STG (Canonical)");
  await expect(viewDialog.getByRole("button", { name: "Close" })).toBeFocused();
  await page.keyboard.press("Escape");
  await expect(viewDialog).not.toBeVisible();
  for (const heading of ["Group", "Concept key", "Languages", "Note", "Actions"]) await expect(page.getByRole("columnheader", { name: heading })).toBeVisible();
  await expect(page.locator("table.translation-glossary-table")).toBeVisible();
  const languagesCell = page.getByRole("row", { name: /staging/ }).locator("td").nth(2);
  await expect(languagesCell).toContainText("ステージング");
  await expect(languagesCell).toContainText("STAGING");
  await expect(languagesCell).toContainText("STG");
  await expect(languagesCell.getByText("Canonical", { exact: true })).toHaveCount(1);
  await expect(languagesCell).toHaveCSS("display", "table-cell");
  const languageSeparator = languagesCell.locator(".translation-glossary-language-group--separated");
  await expect(languageSeparator).toHaveCount(1);
  await expect(languageSeparator).toHaveCSS("border-top-width", "0px");
  await page.getByRole("button", { name: "Add concept" }).click();
  const dialog = page.getByRole("dialog", { name: "Add glossary concept" });
  await expect(dialog).toBeVisible();
  await expect(page.getByText("Terms by language")).toBeVisible();
  await expect(page.locator('input[value="ja"]')).toBeVisible();
  await expect(page.locator('input[value="vi"]')).toBeVisible();
  await dialog.getByLabel("Concept key").fill("release-stage");
  await dialog.getByLabel("ja term 1").fill("ステージング");
  await dialog.getByLabel("vi term 1").fill("STG");
  await dialog.getByRole("button", { name: "Add variant" }).first().click();
  await dialog.getByLabel("ja term 2").fill("STAGING");
  await dialog.getByRole("button", { name: "Save concept" }).click();
  await expect.poll(() => postedConcept).not.toBeNull();
  const postedTerms = ((postedConcept as Record<string, unknown> | null)?.terms as Array<{ language_code: string; is_canonical: boolean }>) || [];
  expect(postedTerms.filter((term) => term.language_code === "ja" && term.is_canonical)).toHaveLength(1);
  expect(postedTerms.filter((term) => term.language_code === "vi" && term.is_canonical)).toHaveLength(1);
  await expect(page.getByText("release-stage")).toBeVisible();
  const createdRow = page.getByRole("row", { name: /release-stage/ });
  await createdRow.locator('summary[aria-label="More actions for release-stage"]').click();
  await createdRow.getByRole("button", { name: "Delete", exact: true }).click();
  const deleteDialog = page.getByRole("dialog", { name: "Delete glossary concept" });
  await expect(deleteDialog).toContainText("release-stage");
  await deleteDialog.getByRole("button", { name: "Delete" }).click();
  await expect.poll(() => deleteCalls).toBe(1);
  await expect(deleteDialog).not.toBeVisible();
  await expect(page.getByRole("cell", { name: "release-stage", exact: true })).not.toBeVisible();
  await page.getByRole("button", { name: "Add concept" }).click();
  const conflictDialog = page.getByRole("dialog", { name: "Add glossary concept" });
  await conflictDialog.getByLabel("Concept key").fill("conflict");
  await conflictDialog.getByLabel("ja term 1").fill("conflict");
  await conflictDialog.getByLabel("vi term 1").fill("conflict");
  await conflictDialog.getByRole("button", { name: "Save concept" }).click();
  await expect(conflictDialog).toBeVisible();
});
